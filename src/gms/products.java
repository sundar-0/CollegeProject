  private int getCatid(Connection con, String catname) throws SQLException{
     PreparedStatement pst=con.prepareStatement("Select catid from category where catname=?"); 
     pst.setString(1,catname);
     ResultSet rs=pst.executeQuery(); 
     rs.next();
     int catid=rs.getInt(1);
     System.out.println(catid);  
     return catid;
  }
  private void addProduct(Connection con,int catid) throws SQLException{
        if(jTextField1.getText().isEmpty())
            {
            JOptionPane.showMessageDialog(null, "All Fields Required"); 
            }
           else
           {
                    PreparedStatement pst=con.prepareStatement("select count(*) as rowcount from product  where prodname=?");
                    pst.setString(1,jTextField1.getText());
                    ResultSet r= pst.executeQuery();
                    r.next();
                    int count = r.getInt("rowcount") ;
                    r.close() ;
                    if(count>0)
                    {
                        JOptionPane.showMessageDialog(null, "Product Already exists");
                     
                    }
                    else
                    {
                     PreparedStatement p;
                     p = con.prepareStatement("insert into product(prodname,mfgdate,expirydate,catidfk) values(?,?,?,?)");
                     p.setString(1,jTextField1.getText()); 
                     p.setString(2,jDateChooser1.getDate().toGMTString().replace("GMT", ""));
                     p.setString(3,jDateChooser2.getDate().toGMTString().replace("GMT", ""));
                     p.setString(4,Integer.toString(catid));
                     p.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Product Inserted Successfully");
                    }
                    clear();
                   
      
           }
  }
