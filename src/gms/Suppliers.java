  //Add Supplier Field
  private void addSupplier(Connection con) throws SQLException{
        if(jTextField1.getText().isEmpty()||jTextField2.getText().isEmpty()||jTextField3.getText().isEmpty()||jTextField4.getText().isEmpty()||jTextField5.getText().isEmpty())     
            {
            JOptionPane.showMessageDialog(null, "All Fields Required"); 
            }
            else{
            PreparedStatement pst=con.prepareStatement("select count(*) as rowcount from supplier  where suppliername=?");
                    pst.setString(1,jTextField1.getText());
                    ResultSet r= pst.executeQuery();
                    r.next();
                    int count = r.getInt("rowcount") ;
                    r.close() ;
                    if(count>0)
                    {
                        JOptionPane.showMessageDialog(null, "Supplier Name Already exists");
                     
                    }
                      else
                    {
                     PreparedStatement p;
                     p = con.prepareStatement("insert into supplier(suppliername,contactperson,phoneno,location,panno,status) values(?,?,?,?,?,?)");
                      p.setString(1,jTextField1.getText()); 
                      p.setString(2,jTextField2.getText()); 
                      p.setString(3,jTextField3.getText()); 
                      p.setString(4,jTextField4.getText()); 
                      p.setString(5,jTextField5.getText()); 
                      p.setString(6,jComboBox2.getSelectedItem().toString());
                      p.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Supplier Name Inserted Successfully");         
                } 
                     clear();
             }
  }
  
