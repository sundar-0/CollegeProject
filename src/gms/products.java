  private void fetchData(String s,Connection con) throws SQLException, ParseException
  {
                 PreparedStatement ps=con.prepareStatement("SELECT * from product join category on product.catidfk=category.catid where prodid=?");
                 ps.setString(1,s);
                 ResultSet rs=ps.executeQuery(); 
                 while(rs.next())
                    {
                    jTextField1.setText(rs.getString(2));
                    jTextField1.setEditable(false);
                    jTextField2.setText(Integer.toString(rs.getInt(1)));
                    jTextField2.setEditable(false);
                    Date mfgdate=new SimpleDateFormat("dd MMM yyyy HH:mm:ss").parse(rs.getString(3)); 
                    jDateChooser1.setDate(mfgdate);
                    jDateChooser1.setEnabled(false);
                    Date expirydate=new SimpleDateFormat("dd MMM yyyy HH:mm:ss").parse(rs.getString(4));
                    jDateChooser2.setDate(expirydate);
                    jDateChooser2.setEnabled(false);
                    jComboBox1.setSelectedItem(rs.getString(7));
                    jComboBox1.setEnabled(false);
                    } 
                jButton3.setEnabled(true);
                jButton4.setEnabled(true);
                jButton5.setEnabled(true);
  
}   
