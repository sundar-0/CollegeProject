//Fetch Data Field
private void fetchData(String s,Connection con) throws SQLException{
      PreparedStatement ps=con.prepareStatement("SELECT * from supplier where supplierid=?");
                 ps.setString(1,s);
                 ResultSet rs=ps.executeQuery(); 
                 while(rs.next())
                    {
                    jTextField7.setText(Integer.toString(rs.getInt(1)));
                    jTextField7.setEditable(false);
                    jTextField1.setText(rs.getString(2));
                    jTextField1.setEditable(false);
                    jTextField2.setText(rs.getString(3));
                    jTextField2.setEditable(false);
                    jTextField3.setText(rs.getString(4));
                    jTextField3.setEditable(false);
                    jTextField4.setText(rs.getString(5));
                    jTextField4.setEditable(false);
                    jTextField5.setText(rs.getString(6));
                    jTextField5.setEditable(false);
                    jComboBox2.setSelectedItem(rs.getString(7));
                    jComboBox2.setEnabled(false);
                    }
                jButton3.setEnabled(true);
                jButton4.setEnabled(true);
                jButton5.setEnabled(true);
                 
    
}
  
